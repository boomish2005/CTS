public class Datatypes {
    public static void main(String[] args) {
        int a=10;
        double c=4.0d;
        float b=20.0f;
        char d='h';
        boolean f=true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(f);
    }
}
